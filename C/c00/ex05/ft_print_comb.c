#include <unistd.h>

void	ft_print_comb(void)
{
	char a[3];

	a[0] = 48;
	while (a[0] < 55)
	{
		a[1] = a[0] + 1;
		while (a[1] < 57)
		{
			a[2] = a[1] + 1;
			while (a[2] < 58)
			{
				write(1, &a[0], 1);
				write(1, &a[1], 1);
				write(1, &a[2], 1);
				write(1, ", ", 2);
				a[2]++;
			}
			a[1]++;
		}
		a[0]++;
	}
	write(1, "789", 3);
}
